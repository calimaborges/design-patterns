package br.com.borgescal.designpatterns.facade.elements;

public class PopcornPopper {

	public void on() {
		System.out.println("PopcornPopper turned on.");
	}

	public void pop() {
		System.out.println("PopcornPopper popping.");		
	}

	public void off() {
		System.out.println("PopcornPopper off.");
	}

}
